# ╔═╡ 7d0096ad-d89a-4ade-9679-6ee95f7d2044
function trygetpixel(img::AbstractMatrix, x::Float64, y::Float64)
	rows, cols = size(img)
	
	# funzione di scaling $f(t) = (t+1)/2$
	f = t -> (t + 1.0)/2.
	
	i = floor(Int, rows * f(-y))
	j = floor(Int, cols * f(x * (rows / cols)))
 
	if 1 < i ≤ rows && 1 < j ≤ cols
		img[i,j]
	else
		# `zero` restituisce lo "zero" del tipo dato
		# `eltype` restituisce il tipo di un *elemento* dell'oggetto dato
		zero(eltype(img)) 
	end
end

